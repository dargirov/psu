#include "adc.h"

void ADC_Init()
{
	P6DIR &= ~BIT4;
	
	/*ADC12CTL0 = ADC12ON;
	ADC12CTL1 = ADC12CSTARTADD_0 | ADC12SSEL_1 | ADC12CONSEQ_0;
	ADC12CTL2 = ADC12RES_2;
	ADC12MCTL0 = ADC12INCH_4 | ADC12SREF_1;
	P6SEL |= BIT4;*/
	
	P6SEL |= BIT4;
	ADC12CTL0 = ADC12ON;
	ADC12CTL1 = ADC12SHP | ADC12SSEL_0;
	ADC12CTL2 = ADC12RES1 | ADC12SR;
	ADC12MCTL0 = ADC12SREF_1 | ADC12INCH_4;
	ADC12CTL0 &= ~ADC12SC;
}